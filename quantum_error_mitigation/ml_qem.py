def generate_data(circuit, nm_params, file_path, items=10000, seed=42):
    """
    Generates data by simulating the given circuit both with and without noise.
    
    :param circuit: The relevant parameterized quantum circuit.
    :param nm_params: The noise model parameters to apply during noisy simulation.
    :param file_path: The path to save the generated data.
    """
    np.random.seed(seed)
    n_params = circuit.num_parameters
    to_save = np.zeros((items, n_params + 2)) # we could add the noise parameters here as well

    all_z = SparsePauliOp('I' * (circuit.num_qubits - 1) + 'Z')
    for i in tqdm(range(items), leave=True):
        anlges = np.random.uniform(-np.pi, np.pi, n_params)
        
        sv = simulate_circ(circuit, anlges, type="statevector", noise_model_params=None)
        dm = simulate_circ(circuit, anlges, type="statevector", noise_model_params=nm_params)
        ideal_exp_val = sv.expectation_value(all_z).real
        noisy_exp_val = dm.expectation_value(all_z).real
        
        to_save[i, :] = np.concatenate([anlges, [ideal_exp_val], [noisy_exp_val]])

    np.savetxt(file_path, to_save, delimiter=',')



